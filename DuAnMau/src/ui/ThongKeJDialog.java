    void fillComboBoxKhoaHoc(){
        DefaultComboBoxModel modelCombo = (DefaultComboBoxModel) cbbKhoaHoc.getModel();
        modelCombo.removeAllElements();
        List<KhoaHoc> list = khdao.selectAll();
        for(KhoaHoc kh : list){
            modelCombo.addElement(kh);
        }
        cbbKhoaHoc.setSelectedIndex(0);
    }
    void fillTableBangDiem(){
        DefaultTableModel model = (DefaultTableModel) tblBangDiem.getModel();
        model.setRowCount(0);
        
        KhoaHoc kh =  (KhoaHoc) cbbKhoaHoc.getSelectedItem(); 
        List<Object[]> list = dao.getBangDiem(kh.getMaKH());
        for(Object[] row : list){
            model.addRow(row);
        }
    }

